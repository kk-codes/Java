public class DataTypes {
    public static void main(String[] args) {
        int num = 58;
        byte by = 127;
        short sh = 23455;
        char c = 'k';
        long ln = 13414115L;
        float fl = 5.8F;
        double db = 5.8;
        boolean bool = true;
        System.out.println(num+":"+by+":"+sh+":"+c+":"+ln+":"+fl+":"+db+":"+bool);
    }
}
